// Spike #52 helper. Simulates the packaging that issue #55 will ship for real:
//
// 1. Adds a "default" condition to every conditional entry in the installed
//    package's exports map. Framework resolves node imports with
//    createRequire().resolve() (CommonJS semantics, conditions
//    ["node", "require", "default"]), so an exports entry offering only
//    {types, import} is invisible to it and the import is silently left as a
//    bare specifier in the built page.
// 2. Strips the side-effect CSS imports from the dist component JS (Framework
//    bundles node imports with Rollup and no CSS plugin, so those imports are
//    a build error) and writes src/sf-components.css, which pulls every
//    component stylesheet in by relative path instead.
//
// Run after every npm install: node scripts/patch-for-spike.mjs
import {readFileSync, writeFileSync, readdirSync, statSync} from "node:fs";
import {join} from "node:path";

const pkgRoot = new URL("../node_modules/@tarassov-ch/swiss-function", import.meta.url).pathname;

// 1. exports conditions
const pkgJsonPath = join(pkgRoot, "package.json");
const pkg = JSON.parse(readFileSync(pkgJsonPath, "utf8"));
let conditions = 0;
for (const value of Object.values(pkg.exports)) {
  if (value && typeof value === "object" && value.import && !value.default) {
    value.default = value.import;
    conditions++;
  }
}
writeFileSync(pkgJsonPath, JSON.stringify(pkg, null, 2));

// 2. strip CSS side-effect imports, collect stylesheets
const cssFiles = [];
let stripped = 0;
(function walk(dir) {
  for (const name of readdirSync(dir)) {
    const path = join(dir, name);
    if (statSync(path).isDirectory()) walk(path);
    else if (name.endsWith(".css")) cssFiles.push(path);
    else if (name.endsWith(".js")) {
      const source = readFileSync(path, "utf8");
      const cleaned = source.replace(/^import\s+"\.\/[^"]+\.css";\s*$/gm, "");
      if (cleaned !== source) {
        writeFileSync(path, cleaned);
        stripped++;
      }
    }
  }
})(join(pkgRoot, "dist"));

const aggregate = [
  "/* Generated by scripts/patch-for-spike.mjs. Aggregate of every component",
  "   stylesheet in the installed package, imported by relative path so",
  "   Framework's esbuild can inline them (issue #52 experiment 4/5). */",
  ...cssFiles
    .filter((f) => f.includes("/dist/components/"))
    .sort()
    .map((f) => `@import url("../node_modules/@tarassov-ch/swiss-function${f.slice(pkgRoot.length)}");`),
].join("\n");
writeFileSync(new URL("../src/sf-components.css", import.meta.url).pathname, aggregate + "\n");

console.log(`exports conditions added: ${conditions}, css imports stripped from ${stripped} js files, ${cssFiles.length} stylesheets aggregated`);
